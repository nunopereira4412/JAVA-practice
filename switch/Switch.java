public class Switch {
	public static void main(String[] args) {

		int switchNumber = 6;

		switch(switchNumber) {
			case 1: 
				System.out.println("Value of 1");
				break;

			case 2: 
				System.out.println("Value of 2");
				break;

			case 3: 
				System.out.println("Value of 3");
				break;

			default:
				System.out.println("No match found");
		}
	}
}